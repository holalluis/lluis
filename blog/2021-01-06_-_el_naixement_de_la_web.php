<h1>El naixement de la web</h1><hr>
<div><code>2021-01-06</code><div><hr><article>

<p>
Tim Berners-Lee, un científic britànic, va inventar la World Wide Web (WWW) el
1989, mentre treballava al CERN. El projecte va ser originalment concebut i
desenvolupat per automatitzar la compartició d'informació entre científics a
universitats i instituts d'arreu del món.
</p>

<img style="max-width:100%" src="adjunts/Screenshot-World-Wide-Web-project-small.png">

<p>
La primera web del CERN (i del món) es va dedicar al projecte World Wide Web
mateix, i es va allotjar a l'ordinador NeXT de Berners-Lee.  El 2013, el CERN
va començar <a href="//info.cern.ch">un projecte (info.cern.ch)</a> per restaurar
<a href="http://info.cern.ch/hypertext/WWW/TheProject.html">la primera web</a>.
</p>

<p>
El 30 d'abril de 1993, el CERN va posar el software World Wide Web al domini
públic. Després, el CERN va publicar-lo amb una llicència oberta, una manera
més segura de maximitzar-ne la seva difusió.  Aquestes accions van permetre a
la web que coneixem avui dia començar la seva història.
</p>

