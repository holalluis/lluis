<h2>Seriosament, què collons més vols?</h2>

<p>
Probablement fas webs i penses que ets especial o alguna merda.
Penses que per la teva puta pàgina inicial, que ocupa 13 megabytes,
i que paral·litza l'explorador, et donaran alguna merda de premi, i que podràs
posar la icona al top corner.
</p></p>
Penses que el putu jquery i els 83 poly-fills fan posar erecte a l'IE7 perquè
finalment té box-shadow. Error, cabró, deixa'm descriure la puta web perfecta:
</p>

<ul>
  <li>És collonudament lleugera i es carrega ràpid.</li>
  <li>Hi cap a totes les merda de pantalles petites.</li>
  <li>Es veu igual a tots els putus exploradors.</li>
  <li>La puta web és accessible a tots els idiotes que la visiten.</li>
  <li>El contingut de merda és llegible i expressa la teva puta idea (si és que
  n'hi ha alguna, més enllà dels 5 megabytes de fotos de hipsters bevent
  cafè).</li>
</ul>

<h3>Doncs saps què, fill de puta:</h3>

<p>Estàs. Sobre. Dissenyant. Mira aquesta merda. És una puta web. Per què
putes necessites animar el putu banner quan passes el ratolí per sobre
aquest tros de merda inútil? Has passat hores, total per afegir 80 kb a la teva
puta web, i tots els idiotes que la miren a l'iPad no veuran mai aquesta
merda. Tampoc cal mencionar els cecs que no veuran mai el banner, però
com a mínim no veuran mai res de la teva merda de web.</p>

<p>Tu no ho sabies, però aquesta és la web perfecta:</p>

<h2>És lleugera</h2>
<p>Aquesta pàgina sencera pesa menys que el teu putu logo del facebook a la
teva pàgina de Wordpress. De debò que carregues 100kb de jquery UI només per
animar el color de fons d'un div? Has carregat 7 fonts d'una puta web de fonts
només per dir "Hola." a una alçada de 100px, al principi de la teva web? Com
pots ser tan merda...

<h2>És responsive</h2>
<p>Idiota.
Pensaves que necessites media queries per ser responsive, però no.
Responsive significa que respon a qualsevol merda de tamany on es veu.
Aquesta web no li importa una merda si la mires des d'un iMac o des d'un putu
tamagotchi.
</p>

<h2>Simplement funciona, joder</h2>

<p>Mira aquesta merda. La pots llegir... si és que pots llegir, fill de puta.
Té sentit. Té una puta jerarquia. Fa servir tags de HTML5, així el teu putu
explorador sap què merda hi ha en aquesta puta web. Això és semàntica, fill de
puta.
</p>

<p>Compatibilitat cross-browser? Carrega aquest cabró a l'IE6 si t'atreveixes,
va, fes-ho, desgraciat.</p>

<h2>Això és una web. Mira-la. No n'havies vist mai cap abans.</h2>

<p>Igual que l'home que no li ha crescut mai la barba no sap quin és el seu
estat natural, tu no tenies ni idea de què era una web.  Tot el que has vist
són merdes de bastarditzacions esqueuomòrfiques sobre què hauria de ser un text
comunicant un putu missatge.  Això és una web real i despullada. Mira-la. És
preciosa, joder.
</p>

<h3>Sí, és una puta sàtira, capullo</h3>

<p>No estic dient que la teva puta web hagi de ser com aquesta.
El que dic és que tots els problemes que tenen les webs són <strong>els que creem
nosaltres mateixos</strong>. Les webs per defecte són funcionals,
ràpides i accessibles. Tú les trenques, sí, tu, fill-de-puta.</p>

