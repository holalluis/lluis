<article><p>
Les màquines escurabutxaques funcionen així:
<ol>
  <li>Poses una moneda.
  <li>Actives una palanca que la fa rodar.
  <li>Obtens una recompensa intermitent variable (res, una mica, molt).
  <li>Tornes a començar.
</ol>

<p>
Un procés que vincula l'accionament d'una palanca amb una recompensa variable
és un procés que maximitza l'addicció.

</p><p>

Alguns desenvolupadors de les xarxes socials més conegudes (facebook, twitter,
instagram, youtube) han reconegut que funcionen amb aquest mateix principi:
</p>

<ol>
  <li>Invertir-hi temps és l'equivalent a posar-hi monedes.
  <li>Totes tenen un mecanisme de "palanca" que genera resultats aleatoris (com
  ara el botó refresh, o l'acció "pull down" que refresca el feed).
  <li>Totes produeixen recompenses intermitents variables (notícies, fotos,
  likes, comentaris, aprovació social, etc).
</ol>

Per tant, tots portem una màquina escurabutxaques a la butxaca: ens dóna
recompenses variables a canvi de petites inversions de temps. Exemples:

<ul>
  <li>Quan obres facebook estàs jugant a veure quines notificacions tens.
  <li>Quan obres el mail estàs jugant a veure quins nous mails tens.
  <li>Quan baixes cap avall a l'instagram estàs jugant a veure quina nova foto sortirà.
  <li>Quan fas dreta/esquerra a apps de dating estàs jugant a veure si tens un match.
  <li>Quan obres la pàgina de notificacions de qualsevol xarxa, estàs jugant a veure què hi ha.
  <li>Quan obres un cofre a jocs com ara Clash Royale estàs jugant a veure què et toca.
</ul>

Aquest comportament addictiu ha sorgit per accident, però al ser identificat
tant efectiu s'ha intentat explotar al màxim. Empreses com Apple, Google,
Facebook, Twitter... en són plenament conscients, i ara tenen la
responsabilitat de reduir aquests efectes de recompensa variable cap a
resultats més previsibles, per allunyar les aplicacions progressivament de la
manera de funcionar de les màquines escurabutxaques.

<p>
Però no val (sempre) culpar-los a ells: és responsabilitat nostra perquè és el <b>nostre</b> temps.

</p><p>

És important <b>aprendre a reconèixer quan una aplicació segueix el mecanisme
d'addicció de "palanca + recompensa variable"</b>, per fugir-ne al màxim, o bé, si
la utilitzem, saber quin perill comporta.

</p><!--fi-->
