<article><p>
M'inquieta molt que el número Pi (π) tingui infinits decimals
(3.141592653589793238462643...). Els decimals no es repeteixen seguint cap
patró, perquè és un número irracional. Això vol dir que en algun punt hi ha
escrit el meu mòbil. I en algun altre punt també hi ha la meva data de
naixement "28071989". Però també en algun altre punt hi ha el meu mòbil seguit
de la meva data de naixement. I al revés, també. Dues vegades. I també tres.
Moltes vegades.  La lletra "h" és "104" en codi ASCII, i la lletra "o" és
"111", i la paraula "hola" és "10411110897". És fàcil intuir ja que la paraula
"hola" està escrita moltes vegades al número Pi. És més: qualsevol llibre que
s'hagi escrit, o encara no, estan dins el número Pi. La meva biografia està
escrita en codi ASCII en algun punt del número Pi. Tots els noms i cognoms de
tota la gent que ha viscut i que viurà al planeta Terra estan escrits al número
Pi. Qualsevol cosa que estiguis pensant ara mateix està escrita al número Pi.
Podries tardar més o menys temps a trobar-ho: però TOT està allà dins. Fins i
tot, dins de Pi també hi ha escrit aquest comentari.
</p><!--fi-->
